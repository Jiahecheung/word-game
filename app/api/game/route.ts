// ---- API 处理 ----

export async function GET(request: NextRequest) {
  const { searchParams } = new URL(request.url);
  const type = searchParams.get('type');

  // 返回关卡列表（只有标题，没有单词内容）
  if (type === 'chapters') {
    return NextResponse.json(
      CHAPTERS.map((ch, i) => ({
        index: i,
        title: ch.title,
        sub: ch.sub,
        wordCount: ch.words.length,
      }))
    );
  }

  // 返回某一关的单词（开始游戏时才请求）
  if (type === 'words') {
    const ci = parseInt(searchParams.get('ci') || '0');
    if (ci < 0 || ci >= CHAPTERS.length) {
      return NextResponse.json({ error: 'Invalid chapter' }, { status: 400 });
    }
    return NextResponse.json({ words: CHAPTERS[ci].words });
  }

  return NextResponse.json({ error: 'Invalid type' }, { status: 400 });
}

export async function POST(request: NextRequest) {
  const body = await request.json();

  // 返回线索（完成关卡后解锁）
  if (body.action === 'clue') {
    const clue = CLUES.find(c => c.ci === body.ci && c.mode === body.mode);
    return NextResponse.json(clue || null);
  }

  // 返回最终答案（全部完成后解锁）
  if (body.action === 'final') {
    return NextResponse.json(FINAL);
  }

  return NextResponse.json({ error: 'Invalid action' }, { status: 400 });
}
